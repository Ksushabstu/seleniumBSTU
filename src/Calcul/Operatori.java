package Calcul;

public class Operatori {
	public static double add(double  scanner, double  scanner1) {
		return scanner +  scanner1;
	}
	
	
	public static double sub(double  scanner, double  scanner1) {
		return scanner -  scanner1;
	}
	
	
	public static double mnog(double  scanner, double  scanner1) {
		return scanner *  scanner1;
	}
	
	public static double delen(double  scanner, double  scanner1) {
		return scanner /  scanner1;
	}

}
